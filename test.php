<h6 class="fw-bold">สายเคเบิลใยแก้วนำแสง MTP® MPO 12F MM OM5</h6>
<p>MTP®/MPO 12F MM OM5 คือสายใยแก้วนำแสงชนิด Multimode (MM) มาตรฐาน OM5 ที่มีจำนวนเส้นใย 12 Core (12F) และใช้หัวคอนเน็กเตอร์แบบ MTP® หรือ MPO ซึ่งเป็นหัวแบบ Multi-Fiber สำหรับงาน Data Center ความเร็วสูง</p>
<h6 class="fw-bold">ประโยชน์และจุดเด่นของ MTP® MPO 12F MM OM5</h6>
<ul>
<li>MPO คือหัวแบบ multi-fiber ที่รวม 12 เส้นใยในหัวเดียว</li>
<li>รองรับความเร็วสูงถึง 40G / 100G / 200G / 400G</li>
<li>รองรับ SWDM ได้ดีที่สุด (เฉพาะ OM5)</li>
<li>ใช้ใน Data Center ความหนาแน่นสูง</li>
</ul>